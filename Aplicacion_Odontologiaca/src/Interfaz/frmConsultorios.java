    public String formarWhere (String Campo, String Valor){
        String Where = "1";       
        if (Campo.equalsIgnoreCase("Numero")){
            Where = "NUMERODECONSULTORIO = "+Valor+"";
        }else if (Campo.equalsIgnoreCase("Piso")){
            Where = "PISO = "+Valor+"";
        }else if (Campo.equalsIgnoreCase("Disponibilidad")){
            Where = "DISPONIBILIDAD LIKE '%"+Valor+"%'";
        }
        return Where;
    }
    
    public void listConsultoriosTabla (ArrayList<Consultorio> arrConsultorios, JTable tabla){
        
        DefaultTableModel modelBase = (DefaultTableModel) tabla.getModel();
        modelBase = limpiarTabla(modelBase);
        for (Consultorio consu : arrConsultorios) {
            Vector tempConsultorio = new Vector();
            tempConsultorio.add(consu.getN_Consultorio());
            tempConsultorio.add(consu.getPiso());
            tempConsultorio.add(consu.getDisponibilidad());
            modelBase.addRow(tempConsultorio);
        }
        tabla.setModel(modelBase);

    }
    
    public DefaultTableModel limpiarTabla (DefaultTableModel model){
        int filas = model.getRowCount();
        for (int i = 0;i<filas; i++) {
            model.removeRow(0); //Eliminacion en pila.
        }
        return model;
    }
    
    
