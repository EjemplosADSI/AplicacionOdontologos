    public static void cargarListaConsultorios(JComboBox combo){
        combo.removeAllItems();
        ArrayList <Consultorio> listConsultorios;
        Consultorio consu = new Consultorio();
        listConsultorios = consu.Buscar("1");
        for (Consultorio con : listConsultorios) {
            combo.addItem(con);
        }
    }

    public static void cargarListaConsultorios(JComboBox combo, int Id){
        combo.removeAllItems();
        ArrayList <Consultorio> listConsultorios;
        Consultorio consu = new Consultorio();
        listConsultorios = consu.Buscar("1");
        for (Consultorio con : listConsultorios) {
            combo.addItem(con);
            if (con.getId() == Id){
                combo.setSelectedItem(con);
            }
        }
    }
    
