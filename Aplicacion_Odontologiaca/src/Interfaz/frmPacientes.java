    private void jBtn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_GuardarActionPerformed
        // TODO add your handling code here:
        try {
            if(validarDatos()){
                String Nombres = jftxtf_Nombres.getText();
                String Apellidos = jftxf_Apellidos.getText();
                long Numero_Documento = Long.parseLong(jftxtf_N_Documento.getText());
                long Telefono = Long.parseLong(jftxtf_Telefono.getText());
                String Correo = jftxtf_Correo.getText();
                String Direccion = jftxtf_Direccion.getText();
                
                Paciente pa = new Paciente();
                if(pa.Nuevo(Nombres, Apellidos, Numero_Documento, Telefono, Correo, Direccion)){
                    JOptionPane.showMessageDialog(rootPane, "Paciente creado con exito", "Paciente Creado", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, "Error al ingresar datos "+ne, "Datos No Validos", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error General "+e, "Datos No Validos ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtn_GuardarActionPerformed

