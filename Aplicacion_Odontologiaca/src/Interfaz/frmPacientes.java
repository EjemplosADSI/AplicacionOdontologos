    private void jBtn_Guardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_Guardar1ActionPerformed
        // TODO add your handling code here:
        
        try {
            if(validarDatosEditar()){
                String Nombres = jftxtf_Nombres1.getText();
                String Apellidos = jftxf_Apellidos1.getText();
                long Numero_Documento = Long.parseLong(jftxtf_N_Documento1.getText());
                long Telefono = Long.parseLong(jftxtf_Telefono1.getText());
                String Correo = jftxf_Correo1.getText();
                String Direccion = jftxtf_Direccion1.getText(); 
                 
                Paciente pac = new Paciente();
                pac.Editar(this.idTempo, Nombres, Apellidos, Numero_Documento, Telefono, Correo, Direccion);
                cargarListaPacientes(jcbx_Editar_Pacientes);
            }
        }catch (NumberFormatException ne) {
            JOptionPane.showMessageDialog(rootPane, "Error al ingresar datos "+ne, "Datos No Validos", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error General Editar"+e, "Datos No Validos ", JOptionPane.ERROR_MESSAGE);
        }
                
    }//GEN-LAST:event_jBtn_Guardar1ActionPerformed

