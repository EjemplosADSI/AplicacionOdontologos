    public static void cargarListaPacientes(JComboBox combo){
        combo.removeAllItems();
        ArrayList <Paciente> listPacientes;
        Paciente pacie = new Paciente();
        listPacientes = pacie.Buscar("1");
        for (Paciente pac : listPacientes) {
            combo.addItem(pac);
        }
    }

    public static void cargarListaPacientes(JComboBox combo, int Id){
        combo.removeAllItems();
        ArrayList <Paciente> listPacientes;
        Paciente pacie = new Paciente();
        listPacientes = pacie.Buscar("1");
        for (Paciente pac : listPacientes) {
            combo.addItem(pac);
            if(pac.getId() == Id){
                combo.setSelectedItem(pac);
            }
        }
    }
    
