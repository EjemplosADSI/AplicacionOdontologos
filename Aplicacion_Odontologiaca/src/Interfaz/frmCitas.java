    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String Campo = jcb_Buscar_Campo.getSelectedItem().toString();
        String Termino = jTxt_Busqueda_Basica.getText();
        Citas ct = new Citas();
        ArrayList<Citas> arrCitas = new ArrayList<Citas>();
        String where = this.formarWhere(Campo, Termino);
        if (Campo.equalsIgnoreCase("Paciente")){
            arrCitas = ct.BuscarVistaCita(where,"citasvspaciente");
        }else if (Campo.equalsIgnoreCase("Odontologo")){
            arrCitas = ct.BuscarVistaCita(where,"citasvsodontologo");
        }else if (Campo.equalsIgnoreCase("Consultorio")){
            arrCitas = ct.BuscarVistaCita(where,"citasvsconsultorio");
        }else{
            arrCitas = ct.Buscar(where);
        }
        listCitasTabla(arrCitas, jTbl_Buscar);
    }//GEN-LAST:event_jButton1ActionPerformed

