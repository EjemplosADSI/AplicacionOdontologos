    private void jBtn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_EliminarActionPerformed
        // TODO add your handling code here:
        if (this.idTempoEliminar != 0){
            int confirmado = JOptionPane.showConfirmDialog(this, "¿Deseas Realmente Eliminar la cita?","Eliminar Cita",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if (JOptionPane.YES_OPTION == confirmado){
                Citas odTemp = new Citas();
                if(odTemp.Eliminar(idTempoEliminar)){
                    JOptionPane.showMessageDialog(rootPane, "La cita se ha eliminado correctamente de la base de datos", "Eliminar Cita", JOptionPane.INFORMATION_MESSAGE);
                    limpiarCamposfrmEdit();
                    cargarListaCitas(jCbx_Eliminar_Cita);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "NO se ha podido eliminar la cita", "Error Eliminar Cita", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Operacion Cancelada", "Eliminar Cita", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jBtn_EliminarActionPerformed

