    public String formarWhere (String Campo, String Valor){
       String Where = "1";       
        if (Campo.equalsIgnoreCase("Fecha")){
            Where = "FECHA = '"+Valor+"'";
        }else if (Campo.equalsIgnoreCase("Hora Inicio")){
            Where = "HORADEINICIO = '"+Valor+"'";
        }else if (Campo.equalsIgnoreCase("Hora Fin")){
            Where = "HORAFIN = '"+Valor+"'";
        }else if (Campo.equalsIgnoreCase("Tratamiento")){
            Where = "TRATAMIENTO LIKE '%"+Valor+"%'";
        }else if (Campo.equalsIgnoreCase("Paciente")){
            Where = "NOMBRES LIKE '%"+Valor+"%'";
        }else if (Campo.equalsIgnoreCase("Odontologo")){
            Where = "NOMBRES LIKE '%"+Valor+"%'";
        }else if (Campo.equalsIgnoreCase("Consultorio")){
            Where = "NUMERODECONSULTORIO = "+Valor+"";
        }
        return Where;
    }   
    
